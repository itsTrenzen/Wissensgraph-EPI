    "- **19. Mai 1955:** James Arthur Gosling wird in Calgary, Alberta, Kanada, geboren.\n\n"

    "- **1977:** Gosling schließt sein Studium an der University of Calgary ab, wo er Informatik studiert hat.\n\n"

    "- **1983:** Er schließt sein Doktoratsstudium an der Carnegie Mellon University ab, wo er an der Entwicklung von Emacs (einem Texteditor) beteiligt war.\n\n"

    "- **1984:** Gosling tritt Sun Microsystems bei, einem Unternehmen, das später von Oracle übernommen wird.\n\n"

    "- **1991:** Gosling leitet das Green-Projekt bei Sun Microsystems, das später zu Java führt.\n\n"

    "- **1995:** Die Programmiersprache Java wird von Gosling und seinem Team bei Sun Microsystems veröffentlicht. Java wurde für plattformunabhängige Anwendungen entwickelt und hatte einen großen Einfluss auf die Softwareentwicklung.\n\n"

    "- **2004:** Gosling verlässt Sun Microsystems, nachdem es von Oracle übernommen wurde.\n\n"

    "- **2011:** Gosling tritt Google bei, verlässt das Unternehmen jedoch im selben Jahr.\n\n"

    "- **2011:** Gosling arbeitet später bei Liquid Robotics, einem Unternehmen für autonome Meeresrobotik.\n\n"

    "- **2017:** Er wird Mitglied des Amazon Web Services (AWS)-Teams.\n\n"



