           "Jahren formuliert und lautet: \"Die Klasse der Turing-berechenbaren Funktionen stimmt mit der Klasse der "
           "intuitiv berechenbaren Funktionen überein.\" . Beide Forscher kamen zu dem Schluss, dass das "
           "Lambda-Kalkül von Church und die Turing-Maschine gleichermaßen alle intuitiv berechenbaren Funktionen "
           "repräsentieren können. Diese Erkenntnis legt nahe, dass die Turing-Maschine als universelles "
           "Berechnungsmodell fungiert und die Church-Turing-These auf alle intuitiv berechenbaren Prozesse anwendbar "
           "ist. Obwohl die These nicht beweisbar ist, da \"intuitiv berechenbare Funktionen\" nicht klar definiert "
           "sind, bildet sie die Basis für das Verständnis von algorithmischen Grenzen und universeller "
           "Berechenbarkeit in der theoretischen Informatik.\n\n"

