"-Dafür schrieb Weizenbaum das Programm MAD-SLIP, die das CTSS-System des MIT nutzte. \n"
"-Dabei hat das System nach bekannten Wörtern gesucht und damit Verknüpfungen erstellt und ähnliche Wörter ausgegeben (z.B. Vater und Familie). \n"
"-Das Prinzip von ELIZA ist die Grundidee von heutigen Chatbots und damit auch ein wichtiger Bestandteil, der Geschichte von künstlicher Intelligenz. \n\n"
