"-das Projekt wurde 1977 von Niklaus Wirth an der ETH Zürich gestartet und hatte bis 1984 mehrere hundert Workstations im Einsatz\n"
"-die Workstation verfügte über einen hochauflösenden Vollseitenbildschirm, eine Maus, eine Laserdrucker-Schnittstelle und eine Netzwerkschnittstelle\n"
"-die Software wurde vollständig in Modula-2 geschrieben und enthielt ein relationales Datenbankprogramm namens Lidas\n"
"-die Uhrfrequenz von Lilith betrug 7 MHz, was eine Ausführungsgeschwindigkeit von 1 bis 2 MIPS ermöglichte\n"
"-der Hauptspeicher war ursprünglich für 65.536 16-Bit-Wörter geplant, wurde aber bald auf die doppelte Kapazität erweitert\n"
"-für reguläre Modula-2-Programme standen jedoch nur die ersten 65.536 Wörter für die Speicherung von Variablen zur Verfügung\n"
"-die Entwicklung von Lilith wurde vom Xerox Alto aus dem Xerox PARC beeinflusst, wo Wirth von 1976 bis 1977 arbeitete\n"
"-da es nicht möglich war, einen Alto nach Europa zu bringen, entschied sich Wirth, ein neues System von Grund auf zu bauen\n"
"-das System wurde unter dem Firmennamen „DISER“ (Data Image Sound Processor and Emitter Receiver System) verkauft\n"
"-das Lilith-Betriebssystem „Medos-2 wurde an der ETH Zürich von Svend Erik Knudsen mit Beratung von Wirth entwickelt\n"
"-dieses Betriebssystem ist ein Single-User-basiertes Betriebssystem, das aus Modula—Modulen besteht\n"
"-das Design hat auch das Betriebssystem „Excelsior“ beeinflusst, das vom Kronos Research Group (KRG) für die sowjetische Kronos-Workstation entwickelt wurde\n"
"-von 1986 bis Anfang der 1990er Jahre entwickelten Ingenieure in der Sowjetunion eine Serie von Leiterplattensystemen und Workstations namens Kronos, basierend auf Lilith\n"
